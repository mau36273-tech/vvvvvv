#!/usr/bin/env node
// Build the single-file Cloudflare Worker that serves the FaceMax site at
// facemaxaiapp.com (script id: restless-brook-47da).
//
// Inputs:
//   web/index.html
//   web/js/native-bridge.js
//   scripts/logo-b64.txt   (snapshot of the logo embedded in the live worker)
//
// Output:
//   dist/worker.js         (ESM module, ready to upload via wrangler / API)

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const indexHtml = readFileSync(resolve(repoRoot, "web/index.html"), "utf8");
const bridgeJs  = readFileSync(resolve(repoRoot, "web/js/native-bridge.js"), "utf8");
const logoB64   = readFileSync(resolve(repoRoot, "scripts/logo-b64.txt"), "utf8").trim();

const jsString = s => JSON.stringify(s);

const worker = `// Auto-generated by scripts/build-cf-worker.mjs — DO NOT EDIT BY HAND.
// Source: web/index.html + web/js/native-bridge.js
const INDEX_HTML = ${jsString(indexHtml)};
const BRIDGE_JS  = ${jsString(bridgeJs)};
const LOGO_B64   = ${jsString(logoB64)};

function decodeLogo() {
  const bin = atob(LOGO_B64);
  const u8 = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) u8[i] = bin.charCodeAt(i);
  return u8;
}

export default {
  async fetch(request) {
    const url = new URL(request.url);
    const p = url.pathname;
    if (p === "/js/native-bridge.js") {
      return new Response(BRIDGE_JS, {
        headers: {
          "content-type": "application/javascript; charset=utf-8",
          "cache-control": "public, max-age=300",
        },
      });
    }
    if (p === "/assets/logo.png" || p === "/logo.png" || p === "/favicon.ico" || p === "/apple-touch-icon.png") {
      return new Response(decodeLogo(), {
        headers: {
          "content-type": "image/png",
          "cache-control": "public, max-age=86400",
        },
      });
    }
    return new Response(INDEX_HTML, {
      headers: {
        "content-type": "text/html; charset=utf-8",
        "cache-control": "public, max-age=0, must-revalidate",
      },
    });
  },
};
`;

const outDir = resolve(repoRoot, "dist");
mkdirSync(outDir, { recursive: true });
const outPath = resolve(outDir, "worker.js");
writeFileSync(outPath, worker);
console.log(`wrote ${outPath} (${worker.length} bytes)`);
